/**
 * Kotlin Custom Signer Example
 *
 * This example demonstrates how to implement a custom cryptographic signer in Kotlin
 * that uses BouncyCastle or other JVM cryptography libraries instead of the built-in
 * Rust implementation.
 *
 * This allows you to:
 * - Use Kotlin/Java's native cryptographic libraries
 * - Integrate with Android Keystore for hardware-backed keys
 * - Use biometric authentication on Android
 * - Implement custom key management strategies
 */

